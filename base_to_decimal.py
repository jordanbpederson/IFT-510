# Converting numbers from different bases to decimal (like converting measurements)

binary_num = "11001" # Measurement in binary

octal_num = "31" # Measurement in octal

hex_num = "19" # Measurement in hexadecimal



# Convert measurements to base 10 (like converting units)

print(f"Decimal from binary {binary_num}: {int(binary_num, 2)}")

print(f"Decimal from octal {octal_num}: {int(octal_num, 8)}")

print(f"Decimal from hexadecimal {hex_num}: {int(hex_num, 16)}")